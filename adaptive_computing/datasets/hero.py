from adaptive_computing.datasets import DatasetBase

from hero import HeroClient, get_env_variable
import threading
import time
import numpy as np
import sys
from adaptive_computing.hero_utils.set_hero_env_vars import set_hero_env_vars

# Set this event from outside (e.g. a manager watchdog) to abort hero_wait_for_data().
_abort_event = threading.Event()

class HeroDataset(DatasetBase):
    def __init__(self, params, machine_names, output_field_path, n_fidelity=1, blocking=False, 
                 task_formatter=None, nan_behavior='mask_ignore'):
        """
        Initialize HeroDataset for distributed task execution.
        
        Args:
            params: Parameter definitions
            machine_names: Available machine names
            n_fidelity: Number of fidelity levels
            blocking: Whether to block on task completion
            output_field_path: Required path to desired output field in worker responses.
                Examples:
                - Simple field name: 'cost'
                - Nested path: 'Task.response.cost', 'Task.outputs.value', etc.
            task_formatter: Optional function to convert x_data arrays to worker-specific task format.
                Should take (x_data_array) and return metadata dict for the worker.
            nan_behavior: Behavior when encountering NaN values ('fail', 'mask_ignore')
        """
        self.blocking = blocking
        self.output_field_path = output_field_path
        self.task_formatter = task_formatter  # Custom task formatting for specific workers
        super().__init__(params, n_fidelity=n_fidelity, nan_behavior=nan_behavior)

        # Define HERO environment variables and queue name
        set_hero_env_vars()

        try:
            HERO_ENV = get_env_variable('HERO_ENV', 'dev')
            HERO_PROJECT = get_env_variable('HERO_PROJECT')
            HERO_QUEUE = get_env_variable('HERO_QUEUE')
        except EnvironmentError as e:
            print(e)
            exit(1)

        APPLICATION_ID = f'{HERO_ENV}-{HERO_PROJECT}'
        
        # Setup the HERO client and authenticate
        self.hero_authenticate(machine_names)

        # TODO: clear_hero_queue() wipes ALL existing tasks (ready, running, error, done)
        # on every startup, making it impossible to resume a run after a crash.
        # Once debugging is complete, replace this with selective cleanup (e.g. only
        # clear 'ready' tasks, and optionally re-queue 'running' ones) or remove
        # entirely and let the startup reconciliation in manager.py handle stale state.
        self.clear_hero_queue()

        # variables used to track hero data:
        # boolean indicating if the sample's output is a valid function return value (False indicates a placeholder value estimated by the surrogate)
        # _unmasked_data is inherited from DatasetBase and will be shape [n_samples, n_out] per fidelity
        # This provides unified masking interface across all dataset types
        # Note: _unmasked_data is inherited from DatasetBase and initialized there
        
        # boolean indicating if the sample evaluation is queued to be run by a Hero manager
        self._hero_todo = [np.empty([0,1],dtype=bool)]*self.n_fidelity # _hero_todo is a list of length n_fidelity. Each entry will be an n_samp[i_fl] x 1 np array
        # string generated by hero to identify each simulation run's inputs and outputs
        self._hero_task_id = [np.empty([0,1], dtype='<U72')]*self.n_fidelity # _hero_task_id is a list of length n_fidelity. Each entry will be an n_samp[i_fl] x 1 np array

    def _extract_field_from_task_data(self, task_data):
        """
        Extract the desired output field from Hero task data using the configured output_field_path.
        
        Args:
            task_data: Hero task data dictionary
            
        Returns:
            tuple: (y_eval_data, field_used) or (None, None) if not found
        """
        # Use the single user-specified path
        y_eval_data, field_used = self._try_field_path(task_data, self.output_field_path)
        if y_eval_data is not None:
            print(f'  Using {field_used}: {y_eval_data}')
            return y_eval_data, field_used
        
        # If the user-specified path didn't work, fail explicitly
        print(f'Error: Could not find output data using specified path: {self.output_field_path}')
        print(f'Available metadata keys: {list(task_data.get("metadata", {}).keys())}')
        return None, None
    
    def _try_field_path(self, task_data, field_path):
        """
        Try to extract a field from task data using a simple path.
        
        Args:
            task_data: Hero task data dictionary
            field_path: String path like 'y_data', 'Task.response.cost', etc.
            
        Returns:
            tuple: (y_eval_data, field_used_description) or (None, None) if not found
        """
        try:
            # Navigate through the path by splitting on '.'
            current_obj = task_data["metadata"]
            path_parts = field_path.split('.')
            
            for part in path_parts:
                if isinstance(current_obj, dict) and part in current_obj:
                    current_obj = current_obj[part]
                else:
                    return None, None
            
            # Convert result to list format
            if current_obj is None:
                return None, None
            elif isinstance(current_obj, (list, tuple)):
                return list(current_obj), field_path
            else:
                return [current_obj], field_path
                
        except Exception as e:
            print(f"  Error extracting field '{field_path}': {e}")
            return None, None

    def hero_authenticate(self, machine_names):
        # Define HERO environment variables and queue name
        set_hero_env_vars()
        try:
            HERO_ENV = get_env_variable('HERO_ENV', 'dev')
            HERO_PROJECT = get_env_variable('HERO_PROJECT')
            HERO_QUEUE = get_env_variable('HERO_QUEUE')
        except EnvironmentError as e:
            print(e)
            exit(1)
        APPLICATION_ID = f'{HERO_ENV}-{HERO_PROJECT}'
        
        hero = HeroClient()
        self.task_engine = hero.TaskEngine(APPLICATION_ID)
        try:
            hero.authenticate()
        except Exception as e:
            print(f"ERROR: HERO authentication failed: {e}")
            sys.exit(1)
        
        # one queue for each fidelity
        self.hero_objs = np.array([], dtype=object)
        for i_fl in range(self.n_fidelity):
            # Try to find existing active queue first (like model.py does)
            queue_name = HERO_QUEUE if i_fl == 0 else HERO_QUEUE + str(i_fl)
            try:
                # Look for existing active queue (AWS worker approach)
                queue_record = self.task_engine.read_queue_by_name(name=queue_name, state="active")
                print(f"Found existing active queue: {queue_name}")
                self.hero_objs = np.append(self.hero_objs, queue_record)
            except Exception:
                # Fallback: create new queue if no active queue exists
                print(f"No active queue found, creating new queue: {queue_name}")
                new_queue = self.task_engine.add_queue(name=queue_name)
                self.hero_objs = np.append(self.hero_objs, new_queue)

        self.machine_names = machine_names
    
    # TODO: remove or replace with resume-friendly cleanup (see TODO above calling site).
    # Clear out any existing Hero tasks
    def clear_hero_queue(self):
        for i_fl in range(self.n_fidelity):
            ready_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='ready')
            running_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='running')
            error_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='error')
            done_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='done')
            for task_record in ready_task_records:
                self.task_engine.delete_task(task_id=task_record['id'])
            for task_record in running_task_records:
                self.task_engine.delete_task(task_id=task_record['id'])
            for task_record in error_task_records:
                self.task_engine.delete_task(task_id=task_record['id'])
            for task_record in done_task_records:
                self.task_engine.delete_task(task_id=task_record['id'])

        self.print_hero_queue()

    def add_samples(self, x_data, i_fidelity):
        """
        Queues new samples in the Hero task system for asynchronous evaluation.
        Placeholder y values (NaN) are stored until the manager returns real results.
        Use add_known_samples() instead if you already have y values.

        Args:
            x_data (N Samples, N input dimensions) array: The input data.
            i_fidelity (int): The fidelity level of the data.
        """
        x_data = np.asarray(x_data)

        self._validate_input(x_data, i_fidelity)

        new_task_ids = np.full((len(x_data), 1), '', dtype='<U72') # strings of length 72 characters or less
        for i, x_data_i in enumerate(x_data):
            if x_data_i.shape[0] != self.n_in:
                print("Point has incorrect number of input dimensions")
                print(f"Point shape: {x_data_i.shape}")
                print(f"Target input dimension: [{self.n_in}]")
                raise ValueError 
            
            # Use custom task formatter if provided, otherwise use default format
            if self.task_formatter is not None:
                task_metadata = self.task_formatter(x_data_i)
                # Ensure original x_data is preserved for internal tracking
                task_metadata['x_data'] = x_data_i.tolist()
            else:
                # Default HeroDataset task format
                task_metadata = {
                    'x_data': x_data_i.tolist(),
                    'y_data': None,
                    'scheduler_job_id': {machine: -1 for machine in self.machine_names},
                    'running': {machine: False for machine in self.machine_names}
                }
                
            new_task = self.task_engine.add_task(
                queue_id=self.hero_objs[i_fidelity]['id'],
                name='Test',
                metatype='Task',
                metadata=task_metadata
            )
            new_task_ids[i] =new_task['id']
            print(f'Task submitted to Hero queue with x_data={x_data_i}')

        self._x_data[i_fidelity] = np.concatenate([self._x_data[i_fidelity], x_data])
        y_data = np.full((len(x_data), self.n_out), np.nan)
        self._y_data[i_fidelity] = np.concatenate([self._y_data[i_fidelity], y_data])
        
        # For Hero tasks, all initial data is masked (unmasked=False) since it's placeholder data
        false_values = np.full((len(x_data), self.n_out), False)
        self._unmasked_data[i_fidelity] = np.concatenate([self._unmasked_data[i_fidelity], false_values])
        
        true_values = np.full((len(x_data), 1), True)
        self._hero_todo[i_fidelity] = np.concatenate([self._hero_todo[i_fidelity], true_values])
        self._hero_task_id[i_fidelity] = np.concatenate([self._hero_task_id[i_fidelity], new_task_ids])

        if self.blocking:
            self.hero_wait_for_data()

    def add_known_samples(self, x_data, y_data, i_fidelity):
        """
        Validates and adds new samples with known output values to the dataset.
        No Hero task is created — use this for pre-computed or experimental data.

        Args:
            x_data (N Samples, N input dimensions) array: The input data.
            y_data (N Samples, N output dimensions): The known output data.
            i_fidelity (int): The fidelity level of the data.
        """
        self._validate_input(x_data, i_fidelity)
        x_data, y_data, unmasked_data = self._validate_data(x_data, y_data, i_fidelity)
        
        # For non-Hero data, keep all data (including masked points) but track which are valid
        self._x_data[i_fidelity] = np.concatenate([self._x_data[i_fidelity], x_data])
        self._y_data[i_fidelity] = np.concatenate([self._y_data[i_fidelity], y_data])
        
        # Use the output-dimension-level unmasked_data directly from validation
        self._unmasked_data[i_fidelity] = np.concatenate([self._unmasked_data[i_fidelity], unmasked_data])
        
        false_values = np.full((len(x_data), 1), False)
        self._hero_todo[i_fidelity] = np.concatenate([self._hero_todo[i_fidelity], false_values])
        empty_values = np.full((len(x_data), 1), '')
        self._hero_task_id[i_fidelity] = np.concatenate([self._hero_task_id[i_fidelity], empty_values])

