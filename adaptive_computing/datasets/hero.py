    # Collect data from any sims in the hero queue that have finished.
    # Note: don't forget to retrain any surrogates that depend on this data.
    def hero_sync_avail_data(self, i_fidelity):
        # for all the done tasks, find the task id and 
        for i, todo_value in enumerate(self._hero_todo[i_fidelity]):
            # Check only the tasks that remain in the hero queue
            if todo_value == True: #XXX check todo_value is not a list of True
                task_id = self._hero_task_id[i_fidelity][i][0]
                task_data = self.task_engine.read_task(task_id)
                if task_data["state"] == 'done':
                    print(f'Found a complete task = {task_id} with x_data = {task_data["metadata"]["x_data"]} and y_data = {task_data["metadata"]["y_data"]}')
                    y_eval = np.array(task_data["metadata"]["y_data"])
                    if y_eval.shape[0] != self.n_out:
                        print("Point has incorrect number of output dimensions")
                        print(f"Point shape: {y_eval.shape}")
                        print(f"Target output dimension: [{self.n_out}]")
                        raise ValueError 
                    self._y_data[i_fidelity][i,:] = np.atleast_2d(float(y_eval))
                    #XXX to do: Check for NaNs and out of bounds y_data. and use something like validate to handle this and possibly do masking
                    #for i_o in range(self.n_out):
                    #    self._unmasked_data[i_fidelity][i,i_o] = check_nan_oob(y_eval[i_o],self.ds_ops)
                    assert(~np.is_nan(y_eval))
                    for i_o in range(len(self.n_out)):
                        self._unmasked_data[i_fidelity][i,i_o] = True
                    self.hero_todo[i_fidelity][i] = False

    #########################################################
    # Wait for workers to complete all tasks in Hero queues.
    # Note: don't forget to retrain any surrogates that depend on this data.
    def hero_wait_for_data(self):
        print('Waiting until workers complete all tasks in all hero queues.')
        while True:
            for i_fl in range(self.n_fidelity):
                self.hero_sync_avail_data(i_fl)
            total_in_queue = np.sum([np.sum(arr) for arr in self._hero_todo])
            if total_in_queue > 0:
                print(f'Number of remaining tasks across all hero queues = {total_in_queue}')
                # print the state of the queue
                for i_fl in range(self.n_fidelity):
                    ready_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='ready')
                    running_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='running')
                    done_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='done')
                    print(f'In the queue corresponding to fidelity level {i_fl},')
                    print(f'There are {len(ready_task_records)} in the "ready" state.')
                    print(f'There are {len(running_task_records)} in the "running" state.')
                    print(f'There are {len(done_task_records)} in the "done" state.')
                time.sleep(1)
            else:
                print('Workers are done. All Hero queues are empty.')
                break
