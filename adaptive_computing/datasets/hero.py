from adaptive_computing.datasets import DatasetBase

from hero import HeroClient, get_env_variable
import time
import numpy as np
import sys

try:
    from adaptive_computing.hero_utils.set_hero_env_vars import set_hero_env_vars
    set_hero_env_vars()
except ImportError:
    pass

try:
    HERO_ENV = get_env_variable('HERO_ENV', 'dev')
    HERO_PROJECT = get_env_variable('HERO_PROJECT')
    HERO_QUEUE = get_env_variable('HERO_QUEUE')
except EnvironmentError as e:
    print(f"Warning: {e}. HeroDataset will not function correctly.")
    HERO_ENV = 'dev'
    HERO_PROJECT = None
    HERO_QUEUE = None

if HERO_PROJECT:
    APPLICATION_ID = f'{HERO_ENV}-{HERO_PROJECT}'
else:
    APPLICATION_ID = None

class HeroDataset(DatasetBase):
    def __init__(self, params, machine_names, n_fidelity=1, blocking=False):
        self.blocking = blocking
        super().__init__(params, n_fidelity=n_fidelity)

        # Setup the HERO client and authenticate
        self.hero_authenticate(machine_names)

        self.clear_hero_queue()

        # variables used to track hero data:
        # boolean indicating if the sample's output is a valid function return value (False indicates a placeholder value estimated by the surrogate)
        self._unmasked_data = [np.empty([0,self.n_out],dtype=bool)]*self.n_fidelity # _unmasked_data is a list of length n_fidelity. Each entry will be an n_samp[i_fl] x n_out np array
        # boolean indicating if the sample evaluation is queued to be run by a Hero manager
        self._hero_todo = [np.empty([0,1],dtype=bool)]*self.n_fidelity # _hero_todo is a list of length n_fidelity. Each entry will be an n_samp[i_fl] x 1 np array
        # string generated by hero to identify each simulation run's inputs and outputs
        self._hero_task_id = [np.empty([0,1], dtype='<U72')]*self.n_fidelity # _hero_task_id is a list of length n_fidelity. Each entry will be an n_samp[i_fl] x 1 np array

    def hero_authenticate(self, machine_names):
        hero = HeroClient()
        self.task_engine = hero.TaskEngine(APPLICATION_ID)
        try:
            hero.authenticate()
        except Exception as e:
            print(f"ERROR: HERO authentication failed: {e}")
            sys.exit(1)
        
        # one queue for each fidelity
        self.hero_objs = np.array([], dtype=object)
        for i_fl in range(self.n_fidelity):
            self.hero_objs = np.append(self.hero_objs,self.task_engine.add_queue(name=HERO_QUEUE+str(i_fl)))

        self.machine_names = machine_names
    
    # Clear out any existing Hero tasks
    def clear_hero_queue(self):
        for i_fl in range(self.n_fidelity):
            self.hero_objs[i_fl]
            ready_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='ready')
            running_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='running')
            error_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='error')
            done_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='done')
            for task_record in ready_task_records:
                self.task_engine.delete_task(task_id=task_record['id'])
            for task_record in running_task_records:
                self.task_engine.delete_task(task_id=task_record['id'])
            for task_record in error_task_records:
                self.task_engine.delete_task(task_id=task_record['id'])
            for task_record in done_task_records:
                self.task_engine.delete_task(task_id=task_record['id'])

        self.print_hero_queue()

    def add_samples(self, x_data, y_data, i_fidelity):
        """
        Adds a new sample to the hero queue.
        The y_data is a placehold value (the surrogate's mean values or the argument can be set to None).
        A Hero task is created and tracked.
        hero_update_avail_data() is used to eventually overwrite the placeholder value with the hero result.
        
        Args:
            x_data (N Samples, N input dimensions) array: The input data.
            y_data (N Samples, N output dimensions): The output data placeholder value or None.
            i_fidelity (int): The fidelity level of the data.
        """
        x_data = np.asarray(x_data)
        if y_data is not None:
            y_data = np.asarray(y_data)

        self._validate_input(x_data, i_fidelity)

        new_task_ids = np.full((len(x_data), 1), '', dtype='<U72') # strings of length 72 characters or less
        for i, x_data_i in enumerate(x_data):
            if x_data_i.shape[0] != self.n_in:
                print("Point has incorrect number of input dimensions")
                print(f"Point shape: {x_data_i.shape}")
                print(f"Target input dimension: [{self.n_in}]")
                raise ValueError 
            new_task = self.task_engine.add_task(
                queue_id=self.hero_objs[i_fidelity]['id'],
                name='Test',
                metatype='Task',
                metadata={
                    'x_data': x_data_i.tolist(),
                    'y_data': None,
                    'slurm_job_id': {machine: -1 for machine in self.machine_names},
                    'running': {machine: False for machine in self.machine_names}
                }
            )
            new_task_ids[i] =new_task['id']
            print(f'Task submitted to Hero queue with x_data={x_data_i}')

        self._x_data[i_fidelity] = np.concatenate([self._x_data[i_fidelity], x_data])
        if y_data is None:
            y_data = np.full((len(x_data), self.n_out), np.nan)
        self._y_data[i_fidelity] = np.concatenate([self._y_data[i_fidelity], y_data])
        false_values = np.full((len(x_data), self.n_out), False)
        self._unmasked_data[i_fidelity] = np.concatenate([self._unmasked_data[i_fidelity], false_values])
        true_values = np.full((len(x_data), 1), True)
        self._hero_todo[i_fidelity] = np.concatenate([self._hero_todo[i_fidelity], true_values])
        self._hero_task_id[i_fidelity] = np.concatenate([self._hero_task_id[i_fidelity], new_task_ids])

        if self.blocking:
            self.hero_wait_for_data()

    def add_samples_nohero(self, x_data, y_data, i_fidelity):
        """
        Validates and adds new samples to the dataset.
        The data including both input and output values are immediately added to the dataset.
        No Hero task is created since the provided y_data values are treated as simulation results rather than placeholders.
        
        Args:
            x_data (N Samples, N input dimensions) array: The input data.
            y_data (N Samples, N output dimensions): The output data.
            i_fidelity (int): The fidelity level of the data.
        """
        self._validate_input(x_data, i_fidelity)
        x_data, y_data = self._validate_data(x_data, y_data, i_fidelity)
        self._x_data[i_fidelity] = np.concatenate([self._x_data[i_fidelity], x_data])
        self._y_data[i_fidelity] = np.concatenate([self._y_data[i_fidelity], y_data])
        true_values = np.full((len(x_data), self.n_out), True)
        self._unmasked_data[i_fidelity] = np.concatenate([self._unmasked_data[i_fidelity], true_values])
        false_values = np.full((len(x_data), 1), False)
        self._hero_todo[i_fidelity] = np.concatenate([self._hero_todo[i_fidelity], false_values])
        empty_values = np.full((len(x_data), 1), '')
        self._hero_task_id[i_fidelity] = np.concatenate([self._hero_task_id[i_fidelity], empty_values])

    #########################################################
    # Collect data from any sims in the hero queue that have finished.
    # Note: don't forget to retrain any surrogates that depend on this data.
    def hero_update_avail_data(self, i_fidelity):
        # for all the done tasks, find the task id and 
        for i, todo_value in enumerate(self._hero_todo[i_fidelity]):
            # Check only the tasks that remain in the hero queue
            if todo_value:
                task_id = self._hero_task_id[i_fidelity][i][0]
                task_data = self.task_engine.read_task(task_id)
                if task_data["state"] == 'done':
                    print(f'Found a complete task = {task_id} with x_data = {task_data["metadata"]["x_data"]} and y_data = {task_data["metadata"]["y_data"]}')
                    y_eval = np.array(task_data["metadata"]["y_data"])
                    if y_eval.shape[0] != self.n_out:
                        print("Point has incorrect number of output dimensions")
                        print(f"Point shape: {y_eval.shape}")
                        print(f"Target output dimension: [{self.n_out}]")
                        raise ValueError 
                    
                    x_data, y_eval = self._validate_data(task_data["metadata"]["x_data"], y_eval, i_fidelity)
                    
                    self._y_data[i_fidelity][i,:] = np.atleast_2d(float(y_eval))
                    for i_o in range(self.n_out):
                        self._unmasked_data[i_fidelity][i,i_o] = True
                    self._hero_todo[i_fidelity][i] = False
                    self.task_engine.delete_task(task_id=task_id)

    #########################################################
    # Wait for managers to complete all tasks in Hero queues.
    # Note: don't forget to retrain any surrogates that depend on this data.
    def hero_wait_for_data(self):
        print('Waiting until managers complete all tasks in all hero queues.')
        while True:
            for i_fl in range(self.n_fidelity):
                self.hero_update_avail_data(i_fl)
            
            # print the state of the queue
            self.print_hero_queue()
            
            total_in_queue = np.sum([np.sum(arr) for arr in self._hero_todo])
            if total_in_queue > 0:
                print(f'Number of tasks AC is awaiting for results = {total_in_queue}')
                time.sleep(1)
                
            else:
                print('Tasks are done. All Hero queues are empty.')
                break

    # Print the state of the hero queue
    def print_hero_queue(self):
        for i_fl in range(self.n_fidelity):
            # Read tasks in different states
            ready_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='ready')
            running_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='running')
            error_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='error')
            done_task_records = self.task_engine.read_tasks(queue_id=self.hero_objs[i_fl]['id'], metatype='Task', state='done')

            # Initialize dictionaries to track queued and running tasks for each machine
            queued_machine = {machine: np.array([task['metadata']['slurm_job_id'].get(machine, -1) != -1 for task in ready_task_records]) for machine in self.machine_names}
            running_machine = {machine: np.array([task['metadata']['running'].get(machine, False) for task in running_task_records]) for machine in self.machine_names}

            # Ready jobs
            total_ready_jobs = len(ready_task_records)
            print(f'Total number of ready (queued or unqueued) jobs: {total_ready_jobs}')

            # Queued jobs
            queued_counts = {machine: int(np.sum(queued_machine[machine])) for machine in self.machine_names}  # Ensure counts are integers
            for machine, count in queued_counts.items():
                print(f'  Jobs queued on {machine}: {count}')

            # Running jobs
            running_counts = {machine: int(np.sum(running_machine[machine])) for machine in self.machine_names}  # Ensure counts are integers
            total_running_jobs_sum = sum(running_counts.values())
            print(f'Total number of running jobs: {total_running_jobs_sum}')
            assert(len(running_task_records) == int(sum(running_counts.values()))) # fails if job is running on two machines
            for machine, count in running_counts.items():
                print(f'  Jobs running on {machine}: {count}')
            
            # Check for running jobs on more than one machine
            for task in running_task_records:
                running_on_machines = [machine for machine in self.machine_names if task['metadata']['running'].get(machine, False)]
                if len(running_on_machines) > 1:
                    raise Exception(f"Task {task['id']} is running on more than one machine: {', '.join(running_on_machines)}.")

            # Error jobs
            total_error_jobs = len(error_task_records)
            print(f'Total number of jobs in the error state: {total_error_jobs}')

            # Done jobs
            total_done_jobs = len(done_task_records)
            print(f'Total number of jobs in the done state: {total_done_jobs}')

            # Check for done tasks that are still queued on any other machine
            for task in done_task_records:
                queued_on_machines = [machine for machine in self.machine_names if task['metadata']['slurm_job_id'].get(machine, -1) != -1]
                if len(queued_on_machines) > 1:
                    print(f"Task {task['id']} is done but still queued on: {', '.join(queued_on_machines)}.")
                    raise Exception(f"Error: Task {task['id']} is done but still queued on another machine.")
