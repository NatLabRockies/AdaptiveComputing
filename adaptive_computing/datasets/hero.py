    # Collect data from any sims in the hero queue that have finished.
    # Note: don't forget to retrain any surrogates that depend on this data.
    def hero_update_avail_data(self, i_fidelity):
        # for all the done tasks, find the task id and 
        for i, todo_value in enumerate(self._hero_todo[i_fidelity]):
            # Check only the tasks that remain in the hero queue
            if todo_value:
                task_id = self._hero_task_id[i_fidelity][i][0]
                task_data = self.task_engine.read_task(task_id)
                if task_data["state"] == 'done':
                    # Extract output using configured field path
                    y_eval_data, field_used = self._extract_field_from_task_data(task_data)
                    
                    if y_eval_data is not None:
                        print(f'Found a complete task = {task_id} with x_data = {task_data["metadata"]["x_data"]} and {field_used} = {y_eval_data}')
                        y_eval = np.array(y_eval_data)
                        if y_eval.shape[0] != self.n_out:
                            print("Point has incorrect number of output dimensions")
                            print(f"Point shape: {y_eval.shape}")
                            print(f"Target output dimension: [{self.n_out}]")
                            raise ValueError 
                        
                        x_data, y_eval, unmasked_data = self._validate_data(task_data["metadata"]["x_data"], y_eval, i_fidelity)
                        
                        # Check if any outputs were masked due to NaN or OOB - treat as task error
                        if not np.all(unmasked_data[0,:]):  # unmasked_data is [1, n_out] for single sample
                            masked_outputs = np.where(~unmasked_data[0,:])[0]
                            print(f'Error: Task {task_id} returned invalid data (NaN or OOB) for output dimensions {masked_outputs}')
                            print(f'Raw output data: {y_eval_data}')
                            
                            # Update task to error state in Hero
                            try:
                                self.task_engine.update_task(
                                    task_id=task_id,
                                    state='error',
                                    name=task_data["name"],
                                    metadata=task_data["metadata"]
                                )
                            except Exception as e:
                                print(f"Warning: Could not update task {task_id} to error state: {e}")
                            
                            # Set y_data to NaN for failed tasks
                            for i_o in range(self.n_out):
                                self._y_data[i_fidelity][i, i_o] = np.nan
                            
                            # Remove from todo list
                            self._hero_todo[i_fidelity][i] = False
                            continue
                        
                        # Update the data with the completed task result (all outputs valid)
                        self._y_data[i_fidelity][i,:] = y_eval.flatten()
                        
                        # Update mask with the validation results for each output dimension
                        self._unmasked_data[i_fidelity][i,:] = unmasked_data[0,:]  # unmasked_data is [1, n_out] for single sample
                        
                        self._hero_todo[i_fidelity][i] = False
                        self.task_engine.delete_task(task_id=task_id)
                    else:
                        # Task completed but has no valid response data - treat as error
                        print(f'Error: Task {task_id} has no valid return value in any expected location')
                        print(f'Available metadata: {task_data["metadata"]}')
                        
                        # Update task to error state in Hero (don't delete per user request)
                        try:
                            self.task_engine.update_task(
                                task_id=task_id,
                                state='error',
                                name=task_data["name"],  # Required parameter
                                metadata=task_data["metadata"]  # Keep existing metadata
                            )
                        except Exception as e:
                            print(f"Warning: Could not update task {task_id} to error state: {e}")
                        
                        # Set y_data to NaN for failed tasks
                        for i_o in range(self.n_out):
                            self._y_data[i_fidelity][i, i_o] = np.nan
                        
                        # Remove from todo list 
                        self._hero_todo[i_fidelity][i] = False
                        
                        # Keep task in error state, don't delete it
                        continue
                elif task_data["state"] == 'error':
                    print(f'Found a failed task = {task_id}, setting y_data to NaN')
                    # Set y_data to NaN for failed tasks
                    for i_o in range(self.n_out):
                        self._y_data[i_fidelity][i, i_o] = np.nan
                    
                    # Mark all outputs as masked (unmasked=False) since they contain NaN
                    self._unmasked_data[i_fidelity][i,:] = False
                    
                    # Remove from todo list
                    self._hero_todo[i_fidelity][i] = False
                    # Keep task in error state, don't delete it. This way we can track how many tasks are failing.

